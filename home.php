	<h2>Frequently asked questions?</h2>
	<img src="apbooks.png" alt="apbooks" style="hieght: 400px; width: 400px; float: right; position: relative; padding-left: 10px; ">
			<h3>How long does it take for my exam to be graded?</h3>
				<p>AP exam scores are usually available online in <b style="font-weight: 500;">early July</b>. The college board releases exam scores based on location to ensure optimal site performance. To see the AP score access schedule, visit <a href="https://apscore.collegeboard.org/scores" style="font-size: 15px;" >apscore.org</a>.</p>
			<h3>How do I view my scores?</h3>
				<p>To access scores, go to <a href="https://apscore.collegeboard.org/scores" style="font-size: 15px;" >apscore.org</a> and select "View Your Scores."</p>
				<p>Sign into your College Board account. It is required that you have an account otherwise your scores cannot be accessed.</p>
				<p>If asked, please provide your <b style="font-weight: 500;">password, username, AP number</b> or <b style="font-weight: 500;">student ID</b>.</p>
			<h3>Will I recieve a paper copy of my score report?</h3>
				<p>No, scores will only be available online through your College Board account.</p>
			<h3>If I recieve a bad score, will it hurt my chances of college admission?</h3>
				<p>Most likely not. Nearly 60 percent of all AP Exams are scored 3 or higher, indicating that the majority of AP students are succeeding at college-level course work. When we surveyed 100 admission officers, more than 75 percent indicated that a low score on an AP Exam would NOT harm an applicant’s admission prospects. (However, admission officers at highly selective colleges and universities appeared more likely to consider a low score a concern in the admission process.) When making admission decisions, colleges consider many more factors than just exam scores, including the strength of your course work and your GPA in rigorous courses. Colleges want to see that you are taking the most rigorous course work available to you. By enrolling in AP courses you demonstrate that you are interested in challenging yourself and learning at a college level.</p>
			<h3>What is the cost of the AP examination fee?</h3>
				<p>Each AP test generally costs between 80-100 dollars. Prices gradually increase year to year due to inflation.</p>
			<h3>How many colleges accept AP grades?</h3>
				<p>Most colleges and universities in the U.S and Canada, as well as colleges and universities in over 40 other countries.</p>
				
