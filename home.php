	<h2>What are AP tests?</h2>
		<img src="APexams.png" alt="AP tests" style="height: 400px; width: 475px; position: relative; float: left; padding-right: 10px;">
		<p>AP tests (Advanced Placement Exams) are taken each May by mostly American and Canadian high school students. The tests are a conclusion to a year of AP courses and consist of a combination of multiple choice and free response questions. Instead of traditional assessment scales, the College Board grades each test in context with the specific year's results. It is the students' cumulative performance that sets the grading scale each year.</p>
			<ul>
				<li><b>5</b>: Extremely well qualified</li>
				<li><b>4</b>: Well-qualified</li>
				<li><b>3</b>: Qualified</li>
				<li><b>2</b>: Possibly qualified</li>
				<li><b>1</b>: No recommendation</li>
			</ul>
		<p>Many universities and other institutions of higher learning in the U.S and Canada accept AP test scores as college credit. For most mid-level universities, a score of 3 or higher can net course credits. For high-level institutions such as the Ivies, even a score of 5 may not grant any credits at all.</p>
	<h2>Why should you use an AP score calculator?</h2>	
	
		<p>An AP score calculator simplifies the mess of AP examination month. It is the best way to find out how an exam is scored, what score is realistic for you, and what you should focus on in order to improve. Oftentimes, students are suprised at how easy it is to pass an AP exam, and ecstatic when they realize that a 4 or 5 may be in their reach. Whether your just trying to gauge your practice test performance or guestimating your actual AP score, an AP score calculator is simple and convenient as opposed to using a traditional calculator or working out scores by hand.</p>
		
